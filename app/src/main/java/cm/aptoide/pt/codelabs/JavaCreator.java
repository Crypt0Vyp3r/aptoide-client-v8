package cm.aptoide.pt.codelabs;

public class JavaCreator {

  private final String name;
  private final String id;

  public JavaCreator(String name, String id){

    this.name = name;
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public String getId() {
    return id;
  }
}
